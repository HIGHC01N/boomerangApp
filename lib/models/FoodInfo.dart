class FoodInfo {
  int objectId;
  int calorie;
  int carbon;
  String imageUrl;
  String name;
  double tree;
  int amount;

  FoodInfo({
    required this.objectId,
    required this.calorie,
    required this.carbon,
    required this.imageUrl,
    required this.name,
    required this.tree,
    required this.amount,
  });
}